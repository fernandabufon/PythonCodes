#Escreva um prog que leia um valor em metros e o exiba convertido e centimetros e milímetros
metros = float(input('Escreva o valor em metros: '))
centimetros = metros*100
milimetros = metros*1000
print('Valor em metros: {}\nValor em centímetros: {}\nValor em milímetros: {}'.format(metros, centimetros, milimetros))

